a = int(input("Enter a number: "))
b = int(input("Enter another number: "))
sum = a + b
print("Sum =", sum)
diff = a - b
print("Difference =", diff)
product = a * b
print("Product =", product)
if b == 0:
    print("Division by zero is not possible")
else:
    division = a / b
    print("Division =", division)