import numpy as np

# Define two matrices
A = np.array([[1, 2],
              [3, 4]])

B = np.array([[5, 6],
              [7, 8]])

# Addition
print("Addition:")
print(A + B)

# Subtraction
print("\nSubtraction:")
print(A - B)

# Element-wise Multiplication
print("\nMultiplication:")
print(A * B)

# Element-wise Division
print("\nDivision:")
print(A / B)